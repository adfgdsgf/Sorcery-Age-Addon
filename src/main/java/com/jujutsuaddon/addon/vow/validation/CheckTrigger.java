package com.jujutsuaddon.addon.vow.validation;

/**
 * 违约检测触发器类型
 * Check Trigger
 *
 * 定义什么事件会触发违约检测。
 * 不同的条件类型关注不同的触发器。
 */
public enum CheckTrigger {

    // ==================== 时间相关 ====================

    /** 每游戏刻检查（用于需要实时监控的条件） */
    TICK,

    /** 每秒检查（用于时间类条件，减少性能开销） */
    SECOND,

    // ==================== 技能相关 ====================

    /** 尝试使用技能时 */
    ABILITY_ATTEMPT,

    /** 技能成功释放后 */
    ABILITY_EXECUTED,

    /** 技能被取消时 */
    ABILITY_CANCELLED,

    // ==================== 战斗相关 ====================

    /** 攻击实体时 */
    ATTACK,

    /** 受到伤害时 */
    HURT,

    /** 击杀实体时 */
    KILL,

    /** 死亡时 */
    DEATH,

    // ==================== 状态相关 ====================

    /** 生命值变化时 */
    HEALTH_CHANGE,

    /** 咒力变化时 */
    CURSED_ENERGY_CHANGE,

    /** 获得效果时 */
    EFFECT_ADDED,

    // ==================== 位置相关 ====================

    /** 进入新区块时 */
    CHUNK_ENTER,

    /** 维度变化时 */
    DIMENSION_CHANGE,

    // ==================== 其他 ====================

    /** 手动触发检查 */
    MANUAL
}
